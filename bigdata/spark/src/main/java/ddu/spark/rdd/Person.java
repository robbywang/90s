package ddu.spark.rdd;

import java.io.Serializable;

/**
 * Created by lideyou on 16/5/28.
 * Person class
 */
public class Person implements Serializable {
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
